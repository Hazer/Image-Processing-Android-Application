    public void onCameraViewStarted(int width, int height) {
        mGray = new Mat();
        mRgba = new Mat(height, width, CvType.CV_8UC4);
        mDetector = new ColorBlobDetector();
        mSpectrum = new Mat();
        mBlobColorRgba = new Scalar(255);
        mBlobColorHsv = new Scalar(255);
        SPECTRUM_SIZE = new Size(200, 64);
        CONTOUR_COLOR = new Scalar(255,0,0,255);
    }

    public void onCameraViewStopped() {
        mGray.release();
        mRgba.release();
    }

    public Mat onCameraFrame(CvCameraViewFrame inputFrame) {

        mRgba = inputFrame.rgba();
        //Core.flip(mRgba,mRgba,1);
        Imgproc.cvtColor(mRgba, mRgba, Imgproc.COLOR_BGRA2BGR);
        mGray = inputFrame.gray();

        if (mAbsoluteFaceSize == 0) {
            int height = mGray.rows();
            if (Math.round(height * mRelativeFaceSize) > 0) {
                mAbsoluteFaceSize = Math.round(height * mRelativeFaceSize);
            }
            mNativeDetector.setMinFaceSize(mAbsoluteFaceSize);
        }

        MatOfRect faces = new MatOfRect();

        if (mDetectorType == JAVA_DETECTOR) {
            if (mJavaDetector != null)
                mJavaDetector.detectMultiScale(mGray, faces, 1.1, 2, 2, // TODO: objdetect.CV_HAAR_SCALE_IMAGE
                        new Size(mAbsoluteFaceSize, mAbsoluteFaceSize), new Size());
        }
        else if (mDetectorType == NATIVE_DETECTOR) {
            if (mNativeDetector != null)
                mNativeDetector.detect(mGray, faces);
        }
        else {
            Log.e(TAG, "Detection method is not selected!");
        }

        Rect[] facesArray = faces.toArray();
        Mat mask1;
        Mat sample_img=null;
        for (int i = 0; i < facesArray.length; i++) {
            mask1= mRgba.submat(facesArray[i]);
            try {
                sample_img = Utils.loadResource(this, R.drawable.baby);
            }
            catch (IOException e)
            {

            }
            Imgproc.cvtColor(sample_img, sample_img, Imgproc.COLOR_BGRA2BGR);
            Size size1=new Size(facesArray[i].height,facesArray[i].width);

            Imgproc.resize(sample_img,sample_img,size1);

            //Log.d(new String(sample_img));
            sample_img.copyTo(mask1);



        }



        if (mIsColorSelected) {
            mDetector.process(mRgba);
            List<MatOfPoint> contours = mDetector.getContours();
            Log.e(TAG, "Contours count: " + contours.size());
            Imgproc.drawContours(mRgba, contours, -1, CONTOUR_COLOR,-1);
        }
        Core.flip(mRgba,mRgba,1);

        if(!ks)
        {
            int ddepth = -1; // destination depth. -1 maintains existing depth from source
            int dx = 1;
            int dy = 1;
            Imgproc.Sobel(mRgba, mRgba, ddepth, dx, dy);
        }
        return mRgba;
    }

